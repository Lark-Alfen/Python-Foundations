"""
If-Elif-Else Statement in Python

The if-elif-else statement is used for multiple conditions, checking each in order.
"""

score = 85
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: D or below")

"""
You can have as many elif branches as needed.
"""

