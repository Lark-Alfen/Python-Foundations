"""
Arithmetic Operators in Python

Arithmetic operators are used for mathematical calculations. Here are the main arithmetic operators:
- Addition (+): Adds two values
- Subtraction (-): Subtracts the right value from the left
- Multiplication (*): Multiplies two values
- Division (/): Divides the left value by the right (result is float)
- Floor Division (//): Divides and returns the integer part (no decimals)
- Modulus (%): Returns the remainder after division
- Exponentiation (**): Raises the left value to the power of the right
"""

print("Arithmetic Operators in Python\n")
a = 10
b = 3
print("a + b:", a + b)      # Addition: 10 + 3 = 13
input("Press Enter to continue...")
print("a - b:", a - b)      # Subtraction: 10 - 3 = 7
input("Press Enter to continue...")
print("a * b:", a * b)      # Multiplication: 10 * 3 = 30
input("Press Enter to continue...")
print("a / b:", a / b)      # Division: 10 / 3 = 3.333...
input("Press Enter to continue...")
print("a // b:", a // b)    # Floor division: 10 // 3 = 3
input("Press Enter to continue...")
print("a % b:", a % b)      # Modulus: 10 % 3 = 1
input("Press Enter to continue...")
print("a ** b:", a ** b)    # Exponentiation: 10 ** 3 = 1000

