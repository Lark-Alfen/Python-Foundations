"""
This program demonstrates the basic data types in Python.
"""

# Integer
num = 10 # Will also work with negative numbers like -5
print("num:", num, "type:", type(num))  # int

input("Press Enter to continue...")

# Float
pi = 3.14 # Fractions will also be float, along with 10/5 since it will be evaluated to 2.0
print("pi:", pi, "type:", type(pi))  # float

input("Press Enter to continue...")

# Complex
comp = 2 + 3j
print("comp:", comp, "type:", type(comp))  # complex

input("Press Enter to continue...")

# Boolean
bool = True
print("bool:", bool, "type:", type(bool))  # bool

input("Press Enter to continue...")

# String
message = "Hello, Python!"
print("message:", message, "type:", type(message))  # str

